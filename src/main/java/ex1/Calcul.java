package ex1;

/**
 * Classe Calcul fournit des méthodes pour effectuer des opérations arithmétiques simples.
 */
public class Calcul {

	/**
	 * Calcule la somme de deux entiers.
	 *
	 * @param nombre1 le premier entier
	 * @param nombre2 le deuxième entier
	 * @return la somme de nombre1 et nombre2
	 */
	public int additionner(int nombre1, int nombre2) {
		return nombre1 + nombre2;
	}

	/**
	 * Calcule la différence entre deux entiers.
	 *
	 * @param nombre1 le premier entier
	 * @param nombre2 le deuxième entier
	 * @return la différence entre nombre1 et nombre2
	 */
	public int soustraire(int nombre1, int nombre2) {
		return nombre1 - nombre2;
	}
}
